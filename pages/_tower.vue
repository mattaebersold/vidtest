<!-- Standard Block renderer -->

<template lang='pug'>

.tower: blocks-list(:blocks='page.blocks')

</template>

<!-- ––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––– -->

<script lang='coffee'>

export default
	name: 'Tower'


	# Get Tower data
	asyncData: ({ $notFound, params, $content }) ->

		# Get data
		page = await $content(params.tower || 'home').fetch()
		return $notFound() unless page

		# Set data
		return { page }

</script>

